package BeerRecipe;

public class Yeasts {
	
	
	
	int yeastID; 
	String yeastNames;
	String yeastCatagry;	
	String yeastType;
	String yeastDescription;
	String yeastSupplier;
	String yeastYeastFormatn;
	String yeastCategoryDisplay;
	
	
	
	
	/*
	 * Getters and Setters
	 */
	
	public int getYeastID() {
		return yeastID;
	}
	public void setYeastID(int yeastID) {
		this.yeastID = yeastID;
	}
	public String getYeastNames() {
		return yeastNames;
	}
	public void setYeastNames(String yeastNames) {
		this.yeastNames = yeastNames;
	}
	public String getYeastCatagry() {
		return yeastCatagry;
	}
	public void setYeastCatagry(String yeastCatagry) {
		this.yeastCatagry = yeastCatagry;
	}
	public String getYeastType() {
		return yeastType;
	}
	public void setYeastType(String yeastType) {
		this.yeastType = yeastType;
	}
	public String getYeastDescription() {
		return yeastDescription;
	}
	public void setYeastDescription(String yeastDescription) {
		this.yeastDescription = yeastDescription;
	}
	public String getYeastSupplier() {
		return yeastSupplier;
	}
	public void setYeastSupplier(String yeastSupplier) {
		this.yeastSupplier = yeastSupplier;
	}
	public String getYeastYeastFormatn() {
		return yeastYeastFormatn;
	}
	public void setYeastYeastFormatn(String yeastYeastFormatn) {
		this.yeastYeastFormatn = yeastYeastFormatn;
	}
	public String getYeastCategoryDisplay() {
		return yeastCategoryDisplay;
	}
	public void setYeastCategoryDisplay(String yeastCategoryDisplay) {
		this.yeastCategoryDisplay = yeastCategoryDisplay;
	}
}
